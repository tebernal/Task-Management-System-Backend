package org.teresa.Task_Management_System.enums;

/**
 * UserRole is an enumeration that defines the different roles a user can have
 * within the task management system.
 */
public enum UserRole {

    /**
     * ADMIN role represents users who have full access to the system,
     * including managing tasks, users, and other administrative functionalities.
     */
    ADMIN,

    /**
     * EMPLOYEE role represents users who have limited access to the system,
     * typically focused on their assigned tasks and personal data.
     */
    EMPLOYEE
}
